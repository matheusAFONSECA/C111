# CONJUNTO

# guardando os elementos dentro de um conjunto{}
nomes = {'Goku', 'Vegeta', 'Trunks', 'Gohan'}
print(nomes)

# conjunto mostra os elementos de forma conjunta, ou seja, não mostra na mesma ordem sempre
# não guarda elementos repetidos
# não tem update, ou seja, não atualiza os elementos -> forma rústica é remover o elemento e adicionar o que deseja

# ADD
nomes.add('Bulma')
nomes.add('Goku')

# SELECT
print(nomes)

# DELETE
nomes.remove('Trunks')
